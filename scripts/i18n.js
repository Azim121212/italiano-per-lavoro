function changeLanguage(lang) {
    currentLang = lang;
    document.documentElement.lang = lang;
    document.getElementById('html-root').setAttribute('lang', lang);
    
    // –û–±–Ω–æ–≤–ª—è–µ–º –≤—Å–µ —ç–ª–µ–º–µ–Ω—Ç—ã —Å data-i18n
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (element.tagName === 'INPUT' && element.type === 'submit') {
                element.value = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });
    
    // –û–±–Ω–æ–≤–ª—è–µ–º –∫–Ω–æ–ø–∫—É –ø–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏—è —è–∑—ã–∫–∞
    const langSwitcher = document.getElementById('langSwitcher');
    if (langSwitcher) {
        // –ü–æ–∫–∞–∑—ã–≤–∞–µ–º —Å–ª–µ–¥—É—é—â–∏–π —è–∑—ã–∫ –≤ —Ü–∏–∫–ª–µ: RU -> IT -> EN -> RU
        let nextLang = 'RU';
        if (lang === 'ru') nextLang = 'IT';
        else if (lang === 'it') nextLang = 'EN';
        else if (lang === 'en') nextLang = 'RU';
        langSwitcher.querySelector('span').textContent = nextLang;
    }
    
    // –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤—ã–±–æ—Ä –≤ localStorage
    localStorage.setItem('preferredLanguage', lang);
}

